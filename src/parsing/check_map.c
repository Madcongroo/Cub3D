#include "../../include/cub3d.h"

int	check_map(char **map)
{
	int	i;
	int	j;
	int	count;

	i = -1;
	count = 0;
	while (map[++i])
	{
		j = -1;
		while (map[i][++j])
		{
			if (map[i][0] == '0' && map[i][1] == '\0')
				count++;
			
		}
	}
}
